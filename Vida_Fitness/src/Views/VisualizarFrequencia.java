    private void popularTabela() {
        dtm = (DefaultTableModel) tabelaDeFrequenciaAlunos.getModel();
        dtm.setNumRows(0);
        int tamanhoDaLista = 0;
        String textoColunaFrequencia = "";
        try {
            listaDeAlunos = controllerAluno.getListaDeAlunos();
            if (listaDeAlunos.size() > 0) {
                for (int i = 0; i < listaDeAlunos.size(); i++) {
                    tamanhoDaLista = listaDeAlunos.get(i).getFrequencias().size();
                    if (tamanhoDaLista == 0) {
                        textoColunaFrequencia = "Nenhuma frequência cadastrada";
                    } else {
                        textoColunaFrequencia = controllerAluno.verificarFrequenciaAluno(listaDeAlunos.
                                get(i).getFrequencias().get(listaDeAlunos.get(i).
                                getFrequencias().size() - 1));
                    }
                    dtm.addRow(new Object[]{
                        listaDeAlunos.get(i).getNome(),
                        listaDeAlunos.get(i).getTelefonePrincipal(),
                        listaDeAlunos.get(i).getTelefoneSecundario(),
                        listaDeAlunos.get(i).getEndereco(),
                        textoColunaFrequencia
                    });
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(VisualizarFrequencia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void filtrar(String valor) {
        try {
            TableRowSorter<DefaultTableModel> resultadoFiltro = new TableRowSorter<>(dtm);
            tabelaDeFrequenciaAlunos.setRowSorter(resultadoFiltro);
            resultadoFiltro.setRowFilter(RowFilter.regexFilter(valor));
        } catch (Exception e) {
            System.out.println("Caractere não aceito.");
        }
    }

